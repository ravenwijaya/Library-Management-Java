    public void refresh() {
        list = ctrl.getkaryawan();
        String[][] data = new String[list.size()][8];
        int i = 0;
        for (karyawan li : list) {
            data[i][0] = String.valueOf(li.getIdkaryawan());
            data[i][1] = String.valueOf(li.getUsername());
            data[i][2] = String.valueOf(li.getPassword());
            data[i][3] = String.valueOf(li.getNama());
            data[i][4] = String.valueOf(li.getTgllahir());
            data[i][5] = String.valueOf(li.getAlamat());
            data[i][6] = String.valueOf(li.getNohp());
            data[i][7] = String.valueOf(li.getJeniskel());
  

            i++;
        }
        tabelkaryawan.setModel(new DefaultTableModel(data, new String[]{"idkaryawan", "username","password","nama", 
            "tgllahir", "alamat",
            "nohp","jeniskel"}));

    }

