    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        int kode = Integer.parseInt(idkaryawan.getText().toString());
        String username=user.getText().toString();
        String pw1=new String (pass1.getPassword());
        String pw2=new String(pass2.getPassword());
        String nama = namaa.getText().toString();
        String tgll = tgl.getText().toString();
        String alamat = alamatt.getText().toString();
        String nohp = nohpp.getText().toString();
        String jeniskelamin="";
        if (laki.isSelected()) {
            jeniskelamin="L";
        }else if (perem.isSelected()) {
            jeniskelamin="P";
        }
        if (username.isEmpty()||pw1.isEmpty()||pw2.isEmpty()||nama.isEmpty()||tgll.isEmpty()||alamat.isEmpty()||nohp.isEmpty()||jeniskelamin.isEmpty()) {
           JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
        }
        else if (!pw1.equals(pw2)) {
            JOptionPane.showMessageDialog(null, "password tidak sama!!");
        }else{
        
       
        try {
            int jwb = JOptionPane.showConfirmDialog(null, "Apakah anda Yakin Edit data karyawan ini ?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (jwb == JOptionPane.YES_OPTION) {
                String update = ctrl.Updatekaryawan(username, pw1,kode,nama, tgll,alamat,nohp,jeniskelamin);
                refresh();
                if (update.equals("berhasil update")) {
                    JOptionPane.showMessageDialog(null, "Berhasil Edit data karyawan", "Info", JOptionPane.INFORMATION_MESSAGE);
                    refresh();
                }
            } else {
                return;
            }

        } catch (Exception e) {
        }}
    }//GEN-LAST:event_simpanActionPerformed

