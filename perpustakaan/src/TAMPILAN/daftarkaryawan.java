    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
       String username=user.getText().toString();
        String pw1=new String(pass1.getPassword());
        String pw2= new String(pass2.getPassword());
        String nama = namaa.getText().toString();
        String tgll = tgl.getText().toString();
        String alamat = alamatt.getText().toString();
         String jk="";
         if (laki.isSelected()) {
            jk="L";
        }else if (perem.isSelected()) {
            jk="P";
        }
        String nohp = nohpp.getText().toString();
       boolean cek=false;
        if (setuju.isSelected()) {
            cek=true;
        }
        if(!cek){
            JOptionPane.showMessageDialog(null, "harus setuju");
        }
        else if (username.isEmpty()||pw1.isEmpty()||pw2.isEmpty()||nama.isEmpty()||tgll.isEmpty()||alamat.isEmpty()||nohp.isEmpty()||jk.isEmpty()) {
           JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
        }else if (!pw1.equals(pw2)) {
            JOptionPane.showMessageDialog(null, "password tidak sama!");
        }
    
        else{
       
       kar.setUsername(username);
     kar.setPassword(pw2);
       kar.setNama(nama);
        kar.setTgllahir(tgll);
        kar.setAlamat(alamat);
        kar.setNohp(nohp);
        kar.setJeniskel(jk);
       
        
        boolean tambah = ctrl.inputkaryawan(kar);
        
        if (tambah == true) {
            JOptionPane.showMessageDialog(null, "Berhasil Tambah data", "Info", JOptionPane.INFORMATION_MESSAGE);
            refresh();
        }}
    }//GEN-LAST:event_tambahActionPerformed

