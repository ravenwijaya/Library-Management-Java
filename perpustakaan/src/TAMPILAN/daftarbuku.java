    public void refresh() {
        list = ctrl.getbuku();//memanggil getbuku di CONTROL
        String[][] data = new String[list.size()][6];
        int i = 0;
        for (buku li : list) {
            data[i][0] = String.valueOf(li.getIdbuku());
            data[i][1] = String.valueOf(li.getJudul());
            data[i][2] = String.valueOf(li.getJumlah());
            data[i][3] = String.valueOf(li.getPenulis());
            data[i][4] = String.valueOf(li.getPenerbit());
            data[i][5] = String.valueOf(li.getTahunterbit());
           
  

            i++;
        }
        tabelbuku.setModel(new DefaultTableModel(data, new String[]{"idbuku", "judul","Jumlah","penulis", 
            "Penerbit", "Tahunterbit"
           }));//mencetak data ke tabel

    }
      

