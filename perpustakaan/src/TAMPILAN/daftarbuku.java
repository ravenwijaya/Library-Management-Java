    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        buk.setJudul(judul.getText().toString());
        buk.setPenulis(penulis.getText().toString());
        buk.setPenerbit(penerbit.getText().toString());
        buk.setTahunterbit(tahun.getText().toString());
        buk.setJumlah(Integer.parseInt(jum.getText().toString()));
       //menset sesuai yang diambil di txtfield
        
        boolean tambah = ctrl.inputbuku(buk);//memanggil inputbuku dicontrol
        
        if (tambah == true) {
            JOptionPane.showMessageDialog(null, "Berhasil Tambah data", "Info", JOptionPane.INFORMATION_MESSAGE);
            refresh();//memanggil refresh,menampilkan message diatas
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
   int kode = Integer.parseInt(idbuku.getText().toString());//mengubah string ke integer
   int juml = Integer.parseInt(jum.getText().toString());//mengubah string ke integer
        String jud = judul.getText().toString();
        String pen =penulis.getText().toString();
        String penet = penerbit.getText().toString();
        String tahunn = tahun.getText().toString();
       //mengambil data dari txtfield dan memasukan dalam suatu variabel
        try {
            int jwb = JOptionPane.showConfirmDialog(null, "Apakah anda Yakin Edit data buku ini ?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (jwb == JOptionPane.YES_OPTION) {//jika ya update dijalankan
                String update = ctrl.Updatebuku(kode,jud,pen,penet,tahunn,juml);//mengupdate tabel dengan variabel yang tadi
                refresh();//memanggil refresh
                if (update.equals("berhasil update")) {
                    JOptionPane.showMessageDialog(null, "Berhasil Edit Buku", "Info", JOptionPane.INFORMATION_MESSAGE);//menampilkan message
                    refresh();//memanggil refresh
                }
            } else {
                return;
            }

        } catch (Exception e) {
        }    
    }//GEN-LAST:event_simpanActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        try {//menghapus data sesuai dengan idbuku
            int jwb = JOptionPane.showConfirmDialog(null, "Apakah anda Yakin Menghapus buku ini ?", "Peringatan", JOptionPane.YES_NO_OPTION);
            if (jwb == JOptionPane.YES_OPTION) {//jika jwb =yes maka data yang memiliki idbuku sekian akan dihapus
                String hps = ctrl.hapus(idbuku.getText().toString());
                if (hps.equals("berhasil hapus")) {
                    JOptionPane.showMessageDialog(null, "Berhasil Menghapus Buku", "Info", JOptionPane.INFORMATION_MESSAGE);
                    refresh();//berhasil dhapus lalu tabel direfresh
                }
            }else{
                return;
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_hapusActionPerformed

